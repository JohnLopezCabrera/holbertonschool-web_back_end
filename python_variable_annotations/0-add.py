#!/usr/bin/env python3
"""
This module contains a type-annotated function to sum two floats.
"""


def add(a: float, b: float) -> float:
    """
    Add two floats and returns the total.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The total of both floats.
    """
    return a + b
