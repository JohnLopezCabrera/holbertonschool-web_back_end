#!/usr/bin/env python3
"""
This module contains a function concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and return the result.

    Args:
        str1 (str): The 1st string to join.
        str2 (str): The 2nd string to join.

    Returns:
        str: The resulting combined string.
    """
    return str1 + str2
